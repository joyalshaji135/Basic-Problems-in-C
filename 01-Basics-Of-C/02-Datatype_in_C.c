// Datatypes in C

#include<stdio.h>
int main()
{
    int integer_num = 10;
    float float_num = 10.9;
    double double_num = 189.32892;
    char char_data = 'h';
    printf("integer data : %d\n",integer_num);
    printf("floating point data : %f\n",float_num);
    printf("double data : %lf\n",double_num);
    printf("Character Data : %c\n",char_data);
    return 0;
}